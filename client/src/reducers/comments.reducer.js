import { POST_COMMENT } from "../actions/comments.actions";
import { GET_COMMENT } from './../actions/comments.actions';

const initialState = {};

export default function commentsReducer(state = initialState, action) {
  switch (action.type) {
      
    case POST_COMMENT:
      return action.payload;

    case GET_COMMENT:
      return action.payload;

    default:
      return state;
  }
}
